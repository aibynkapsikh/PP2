# Assignment Operators

# Assignment operators are used to assign values to variables:

#  Operator =
x = 5 # Example
x = 5 # Same As
#  Operator +=
x += 3 # Example
x = x + 3 # Same As
#  Operator -=
x -= 5 # Example
x = x - 5  # Same As
#  Operator *=
x *= 5 # Example
x = x * 5 # Same As
#  Operator /=
x /= 5 # Example
x = x / 5 # Same As
#  Operator %=
x %= 5 # Example
x = x % 5 # Same As
#  Operator //=
x //= 5 # Example
x = x // 5 # Same As
#  Operator **=
x **= 5 # Example
x = x ** 5 # Same As
#  Operator &=
x &= 5 # Example
x = x & 5 # Same As
#  Operator |=
x |= 5 # Example
x = x | 5 # Same As
#  Operator ^=
x ^= 5 # Example
x = x ^ 5 # Same As
#  Operator >>=
x >>= 5 # Example
x =  x >> 5 # Same As
#  Operator <<=
x <<= 5 # Example
x = x << 5 # Same As
#  Operator :=
print(x := 3) # Example
x = 3 
print(x) # Same As


