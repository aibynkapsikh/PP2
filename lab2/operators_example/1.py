# Operators are used to perform operations on variables and values.

# we use the + operator to add together two values:
print(10 + 5)

# + addition 
x = 10
y = 5
print(x + y)

# - Subtraction
print(x - y)

# * Multiplication
print(x * y)

# / Division
print(x / y)

# % Modulus
print(x % y)

# ** Exponentiation
print(x ** y)

# // Floor division
print(x // y)
