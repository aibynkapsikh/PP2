# Python Math

# Built-in Math Functions
# The min() and max() functions can be used to find the lowest or highest value in an iterable:

x = min(5, 10, 15)
y = max(5, 10, 15)

print(x)
print(y)

x = abs(-7.25)

print(x)

x = pow(4, 3)

print(x)

import math

x = math.sqrt(64)
print(x)

x = math.ceil(1.4)
y = math.floor(1.4)

print(x) # returns 2
print(y) # returns 1

x = math.pi

print(x)